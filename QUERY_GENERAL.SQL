SELECT --*,
WAREHOUSE_ID,
IB_ID,
IS_TYPE,
EXTRACT(DATE FROM INB_ARRIVAL_DATETIME) AS ARRIVAL_DATE,
DATA_SLA  AS SLA_DATE,
HOUR_SLA,
SLA_DATE AS SLA_DATETIME,
INB_ARRIVAL_DATETIME,
INB_RECEPTION_DATETIME,
CHECKIN_FIRSTMOV,
CHECKIN_LASTMOV,
PUTAWAY_FIRSTMOV,
PUTAWAY_LASTMOV,
SUM (AGG.UNITS_RECEIVED) AS RECEIVING,
FROM meli-bi-data.WHOWNER.DM_OPS_IB_DETAILS_AGG AGG
LEFT JOIN `meli-bi-data.WHOWNER.DM_INBOUND_SLA_PROCESS_HOUR` SLA ON AGG.IB_ID = CAST(SLA.INBOUND_ID AS INT64)
WHERE WAREHOUSE_ID ="COCU02"
AND EXTRACT(YEAR FROM INB_ARRIVAL_DATETIME) = 2026
--AND EXTRACT(DATE FROM INB_ARRIVAL_DATETIME) = "2026-05-14"
--AND IB_ID =66977503
GROUP BY ALL
;